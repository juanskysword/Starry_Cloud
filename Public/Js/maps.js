function drawMap(level) {
  for (let i = 0; i < level.length; i++) {
    for (let j = 0; j < level[i].length; j++) {
      ctx.drawImage(
        worldTiles,
        (level[i][j] % 18) * 17 + 1,
        Math.floor(level[i][j] / 18) * 17 + 1,
        16,
        16,
        j * 16,
        i * 16,
        16,
        16
      );
    }
  }
}

function collision(x, y, map) {
  for (let i = 0; i < map.length; i++) {
    for (let j = 0; j < map[i].length; j++) {
      if (map[i][j] != 2) {
        if (
          x <= j * 16 + 16 &&
          x + 12 >= j * 16 &&
          y + 10 <= i * 16 + 16 &&
          y + 16 >= i * 16
        ) {
          return true;
        }
      }
    }
  }
  return false;
}